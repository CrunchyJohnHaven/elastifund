@dataclass
class TournamentResult:
    genome_id: str
    fitness: float
    win_rate: float
    total_signals: int
    total_fills: int
    gross_pnl: float
    sharpe: float
    max_drawdown: float
    profit_factor: float
    kelly_fraction: float
    p_value: float
    kill_reasons: list[str] = field(default_factory=list)
    survived: bool = True
    eval_time_ms: float = 0.0
    details: dict[str, Any] = field(default_factory=dict)


@dataclass
class TournamentReport:
    """Summary of a tournament round."""
    tournament_id: str
    started_at: str
    completed_at: str
    population_size: int
    survived_count: int
    killed_count: int
    best_fitness: float
    best_genome_id: str
    median_fitness: float
    results: list[TournamentResult] = field(default_factory=list)
    eval_time_total_ms: float = 0.0
    cores_used: int = 1


