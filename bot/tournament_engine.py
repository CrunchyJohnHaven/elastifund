#!/usr/bin/env python3
"""
Tournament Engine — Parallel Local Backtesting at Scale
========================================================
Takes a population of strategy genomes and evaluates them all against
historical data using every available CPU core. Returns ranked results
with kill-rule enforcement and statistical significance testing.

This is the compute layer. It doesn't decide what to test (that's the
Evolution Loop). It doesn't decide what to promote (that's the Promotion
Manager). It answers one question: given these N strategy variants,
which ones survive?

Architecture:
  - Each genome is evaluated independently (embarrassingly parallel)
  - Each evaluation runs the full backtest pipeline:
    1. Generate signals from genome-weighted sources
    2. Filter by genome-encoded rules
    3. Size positions by genome Kelly/confidence
    4. Simulate fills with maker/taker cost models
    5. Compute fitness: Sharpe × sqrt(fills) × (1 - max_DD)
  - Kill rules applied post-evaluation (not during — let them run)
  - Results ranked by fitness with statistical significance noted

Fitness function rationale:
  Sharpe alone rewards low-activity strategies that get lucky on 3 trades.
  Sharpe × sqrt(fills) rewards strategies that find MANY profitable trades.
  The (1 - max_DD) penalty prevents strategies that are right on average
  but have drawdowns that would blow the account.

March 2026 — Elastifund / JJ
"""
