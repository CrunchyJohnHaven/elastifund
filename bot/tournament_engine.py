class TournamentEngine:
    """
    Runs parallel backtests across a population of genomes.

    Uses all available CPU cores. Each genome is evaluated independently
    against the same historical dataset. Results are ranked by fitness.
    """

    def __init__(
        self,
        data_path: str = "",
        max_workers: Optional[int] = None,
        config: Optional[dict] = None,
    ):
        self.data_path = data_path
        self.max_workers = max_workers or max(1, cpu_count() - 1)
        self.config = config or {}
        self._tournament_count = 0

    def run_tournament(
        self,
        genomes: list[StrategyGenome],
        tournament_id: Optional[str] = None,
    ) -> TournamentReport:
        """
        Evaluate all genomes in parallel and return ranked results.

        Args:
            genomes: Population to evaluate
            tournament_id: Optional identifier for this tournament round

        Returns:
            TournamentReport with ranked results (best first)
        """
        self._tournament_count += 1
        tid = tournament_id or f"T{self._tournament_count:04d}"
        started = datetime.now(timezone.utc).isoformat()
        t0 = time.monotonic()

        logger.info(
            "Tournament %s: evaluating %d genomes on %d cores",
            tid, len(genomes), self.max_workers,
        )

        # Serialize genomes for multiprocessing
        work_items = []
        for genome in genomes:
            genome_dict = {
                "genome_id": genome.genome_id,
                "genes": {
                    k: {"value": g.value, "type": g.gene_type.value,
                         "range": [g.lower, g.upper]}
                    for k, g in genome.genes.items()
                },
            }
            work_items.append((genome_dict, self.data_path, self.config))

        # Run parallel evaluation
        raw_results: list[dict] = []

        if self.max_workers <= 1 or len(genomes) <= 2:
            # Sequential for small batches or debugging
            for item in work_items:
                raw_results.append(_evaluate_genome_worker(item))
        else:
            with ProcessPoolExecutor(max_workers=self.max_workers) as executor:
                futures = {
                    executor.submit(_evaluate_genome_worker, item): i
                    for i, item in enumerate(work_items)
                }
                for future in as_completed(futures):
                    try:
                        raw_results.append(future.result())
                    except Exception as e:
                        idx = futures[future]
                        logger.error("Genome eval %d failed: %s", idx, e)
                        raw_results.append(
                            _empty_result(
                                genomes[idx].genome_id if idx < len(genomes) else "?",
                                f"process_error: {str(e)[:100]}",
                            )
                        )

        # Convert to TournamentResult objects
        results = []
        for r in raw_results:
            results.append(TournamentResult(
                genome_id=r["genome_id"],
                fitness=r["fitness"],
                win_rate=r["win_rate"],
                total_signals=r["total_signals"],
                total_fills=r["total_fills"],
                gross_pnl=r["gross_pnl"],
                sharpe=r["sharpe"],
                max_drawdown=r["max_drawdown"],
                profit_factor=r["profit_factor"],
                kelly_fraction=r["kelly_fraction"],
                p_value=r["p_value"],
                kill_reasons=r.get("kill_reasons", []),
                survived=r.get("survived", False),
                eval_time_ms=r.get("eval_time_ms", 0.0),
                details=r.get("details", {}),
            ))

        # Update genome fitness values
        result_map = {r.genome_id: r for r in results}
        for genome in genomes:
            if genome.genome_id in result_map:
                genome.fitness = result_map[genome.genome_id].fitness

        # Sort by fitness (highest first)
        results.sort(key=lambda r: r.fitness, reverse=True)

        survived = [r for r in results if r.survived]
        killed = [r for r in results if not r.survived]

        elapsed_ms = (time.monotonic() - t0) * 1000
        completed = datetime.now(timezone.utc).isoformat()

        fitnesses = [r.fitness for r in results if r.fitness > -900]
        median_fitness = sorted(fitnesses)[len(fitnesses) // 2] if fitnesses else 0.0

        report = TournamentReport(
            tournament_id=tid,
            started_at=started,
            completed_at=completed,
            population_size=len(genomes),
            survived_count=len(survived),
            killed_count=len(killed),
            best_fitness=results[0].fitness if results else 0.0,
            best_genome_id=results[0].genome_id if results else "",
            median_fitness=median_fitness,
            results=results,
            eval_time_total_ms=elapsed_ms,
            cores_used=self.max_workers,
        )

        logger.info(
            "Tournament %s complete: %d survived, %d killed, "
            "best=%.4f (%s), median=%.4f, %.1fs total",
            tid, len(survived), len(killed),
            report.best_fitness, report.best_genome_id,
            report.median_fitness, elapsed_ms / 1000,
        )

        return report

    def evaluate_single(self, genome: StrategyGenome) -> TournamentResult:
        """Evaluate a single genome (for debugging or incremental evaluation)."""
        report = self.run_tournament([genome], tournament_id="single")
        return report.results[0] if report.results else TournamentResult(
            genome_id=genome.genome_id, fitness=-999.0,
            win_rate=0.0, total_signals=0, total_fills=0,
            gross_pnl=0.0, sharpe=0.0, max_drawdown=1.0,
            profit_factor=0.0, kelly_fraction=0.0, p_value=1.0,
            kill_reasons=["eval_failed"], survived=False,
        )
